package com.ufeyrabagci.hafta4

class MyMath {
    var sayi1 :Int=0
    var sayi2 :Int=0

    fun Toplama(sayi1: Int, sayi2:Int ){
        println("Toplama sonucu = ${sayi1+sayi2}")
    }
    fun Cikarma(sayi1: Int, sayi2:Int ){
        println("Çıkarma sonucu = ${sayi1-sayi2}")
    }
    fun Bolme(sayi1: Int, sayi2:Int ){
        println("Bölme sonucu = ${sayi1/sayi2}")
    }
    fun Carpma(sayi1: Int, sayi2:Int ){
        println("Çarpma sonucu = ${sayi1*sayi2}")
    }
}